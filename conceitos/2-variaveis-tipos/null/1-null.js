// null is an object
const nullVariable = null;

// true is not a object
const trueVariable = true;


console.log(`O valor da variável "nullVariable" é: ${nullVariable} e o tipo é:`, typeof(nullVariable));
// Apezar de ser null
// observamos que seu tipo é um object

console.log(`\nO valor da variável "trueVariable" é: ${trueVariable} e o tipo é:`, typeof(trueVariable));
// Apezar de ser true
// observamos que seu tipo é boolean