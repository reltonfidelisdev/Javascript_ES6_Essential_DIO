// (==) Comparação de Igualdade
// Retorna verdadeiro caso os openandos SEJAM iguais
console.log(1 == true) // true

// (!=) Negação de Igualdade
// Retorna verdadeiro caso os operandos NAO SEJAM iguais
console.log(false != null) // true

// (===) Estritamente iguais
// Retorna verdadeiro caso os operandos SEJAM iguais e do MESMO TIPO
const myTrueVar = true;
console.log(true === myTrueVar) // true

// (===) Estritamente iguais
// Retorna verdadeiro caso os operandos SEJAM iguais e do MESMO TIPO
const myFalseVar = false;
console.log(true !== myFalseVar) // true

// (>) Maior que
// Retorna verdadeiro se o operando da esquerda for maior que o da direita
console.log(23 > 1);

// (>=) Maior ou igual
// Retorna verdadeiro se o operando da esquerda for maior ou igual ao da direita
console.log(23 >= 1);

// (<) Menor que
// Retorna verdadeiro se o operando da esquerda for menor que o da direita
console.log(5 < 10);

// (<=) Maior ou igual
// Retorna verdadeiro se o operando da esquerda for menor ou igual ao da direita
console.log(7 <= 7);


