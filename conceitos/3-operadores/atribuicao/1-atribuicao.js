// (=) Atribuição
x = y;

// (+=) Atribuição de adição
x += y;
// O mesmo que x = x + y

// (-=) Atribuição de subtração
x -= y;
// O mesmo que x = x - y

// (=*) Atribuição de multiplicação
x *= y;
// O mesmo que x = x * y

// (/=) Atribuição de divizão
x /=  y;
// O mesmo que x = x / y

// (%=) Atribuição de resto
x %= y;
// O mesmo que x = x % y
