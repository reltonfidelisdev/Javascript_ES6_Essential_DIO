
// (&&) Operador lógico AND
// Retorna true se as duas condições forem satisfeiras
// Se usuario for abilirado e estiver logado, mostre o menu, senão mostre a tela de login.

var a1 = true &&  true;     // t & t : retorna true
var a2 = true &&  false;     // t & t : retorna true

// (||) Operador lógico OU
// Retorna true se ao menos uma das condições forem satisfeitas
// Se 35 for maior que 35 OU 34 for menor que 35
console.log(35 > 35 || 34 < 35)

// (!) Operador lógico NOT
// utilizado para negar uma ação ou comparação
// Se NÂO for isso ... Faça isso
console.log(!true) // Não verdade : false